}

function Wait-ForKafka {
    Write-Host "Waiting for Kafka..."
    for ($attempt = 1; $attempt -le 60; $attempt++) {
        & docker compose --env-file $EnvFile @ComposeFiles exec -T kafka `
            /opt/kafka/bin/kafka-topics.sh --list --bootstrap-server kafka:19092 *> $null
        if ($LASTEXITCODE -eq 0) {
            return
        }
        Start-Sleep -Seconds 2
    }

    throw "Kafka did not become ready. Run .\quick-docker-setup.cmd logs for details."
}

function New-KafkaTopics {
    $topics = @("market-data", "market-payment", "market-rest", "market-ingestion")
    foreach ($topic in $topics) {
        Invoke-Compose -ComposeArgs @(
            "exec", "-T", "kafka",
            "/opt/kafka/bin/kafka-topics.sh",
            "--create", "--if-not-exists",
            "--topic", $topic,
            "--partitions", "3",
            "--replication-factor", "1",
            "--bootstrap-server", "kafka:19092"
        )
    }
    Write-Host "Kafka topics are ready."
}

function Wait-ForGateway {
    Write-Host "Waiting for the REST health check..."
    for ($attempt = 1; $attempt -le 90; $attempt++) {
        & docker compose --env-file $EnvFile @ComposeFiles exec -T gateway `
            wget -qO- http://localhost/health *> $null
        if ($LASTEXITCODE -eq 0) {
            Write-Host "FinSight is ready."
            return
        }
        Start-Sleep -Seconds 2
    }

    throw "The health check did not pass. Run .\quick-docker-setup.cmd logs for details."
}

function Start-FinSightStack {
    New-LocalEnvironment
    Invoke-Compose -ComposeArgs @("config", "--quiet")

    Write-Host "Starting MySQL, Redis, Kafka, and MQTT..."
    Invoke-Compose -ComposeArgs @(
        "up", "-d", "--wait", "--wait-timeout", "180",
        "mysql", "redis", "kafka", "mosquitto"
    )
    Wait-ForKafka
    New-KafkaTopics

    Write-Host "Building FinSight images. The first build can take several minutes..."
    Invoke-Compose -ComposeArgs @(
        "build", "--pull",
        "market-rest-1", "market-rest-2", "market-ingestion",
        "market-realtime", "market-frontend"
    )

    Write-Host "Starting the core FinSight application..."
    Invoke-Compose -ComposeArgs @(
        "up", "-d",
        "market-realtime", "market-rest-1", "market-rest-2",
        "market-ingestion", "market-frontend", "gateway"
    )

    Wait-ForGateway
    Invoke-Compose -ComposeArgs @("ps")
    Write-Host ""
    Write-Host "FinSight: http://localhost:8080"
    Write-Host "Health:   http://localhost:8080/health"
    Write-Host "Swagger:  http://localhost:8080/api-docs/"
    Write-Host ""
    Write-Host "A new MySQL volume is initialized from the bundled thesis database snapshot."
    Write-Host "Payment and live-market integrations are disabled until configured."
}

try {
    switch ($Action) {
        "setup" {
            Assert-Docker
            Start-FinSightStack
        }
        "status" {
            Assert-Docker
            if (-not (Test-Path -LiteralPath $EnvFile)) {
                throw "Missing $EnvFile. Run .\quick-docker-setup.cmd first."
            }
            Invoke-Compose -ComposeArgs @("ps")
        }
        "logs" {
            Assert-Docker
            if (-not (Test-Path -LiteralPath $EnvFile)) {
                throw "Missing $EnvFile. Run .\quick-docker-setup.cmd first."
            }
            Invoke-Compose -ComposeArgs @("logs", "--tail=200", "-f")
        }
        "stop" {
            Assert-Docker
            if (-not (Test-Path -LiteralPath $EnvFile)) {
                throw "Missing $EnvFile. Run .\quick-docker-setup.cmd first."
            }
            Invoke-Compose -ComposeArgs @("down")
        }
        "help" {
            Show-Usage
        }
    }
}
catch {
    Write-Error $_.Exception.Message
    exit 1
}
