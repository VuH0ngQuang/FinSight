param(
    [ValidateSet("setup", "status", "logs", "stop", "help")]
    [string]$Action = "setup"
)

Set-StrictMode -Version Latest
$ErrorActionPreference = "Stop"

Set-Location $PSScriptRoot

$EnvFile = if ($env:ENV_FILE) { $env:ENV_FILE } else { ".env.quickstart" }
$ComposeFiles = @("-f", "docker-compose.yml", "-f", "docker-compose.local.yml")

function Show-Usage {
    Write-Host @"
Usage: .\quick-docker-setup.ps1 [setup|status|logs|stop]

  setup   Create a local environment, build images, and start FinSight (default)
  status  Show container status
  logs    Follow logs from all local services
  stop    Stop containers without deleting local data

Optional: set ENV_FILE to use another environment file.
"@
}

function Invoke-Compose {
    param([string[]]$ComposeArgs)

    & docker compose --env-file $EnvFile @ComposeFiles @ComposeArgs
    if ($LASTEXITCODE -ne 0) {
        throw "Docker Compose command failed with exit code $LASTEXITCODE."
    }
}

function Assert-Docker {
    if (-not (Get-Command docker -ErrorAction SilentlyContinue)) {
        throw "Docker is not installed. Install and start Docker Desktop first."
    }

    & docker compose version *> $null
    if ($LASTEXITCODE -ne 0) {
        throw "The Docker Compose plugin is not available."
    }

    & docker info *> $null
    if ($LASTEXITCODE -ne 0) {
        throw "Docker Desktop is not running or is not accessible."
    }
}

function New-RandomSecret {
    $bytes = New-Object byte[] 24
    $generator = [System.Security.Cryptography.RandomNumberGenerator]::Create()
    try {
        $generator.GetBytes($bytes)
    }
    finally {
        $generator.Dispose()
    }

    return -join ($bytes | ForEach-Object { $_.ToString("x2") })
}

